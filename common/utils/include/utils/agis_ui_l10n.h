/** 二选一字符串（技术说明类长文本可用，避免枚举爆炸）。 */
inline const wchar_t* AgisPickUiLang(const wchar_t* zh, const wchar_t* en) {
  return AgisGetUiLanguage() == AgisUiLanguage::kEn ? en : zh;
}

